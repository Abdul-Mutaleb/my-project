<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Model;

class OrderCount extends Model
{
    use HasFactory;
    protected $fillable = [
        'order_id',
        'product_id',
        'quantity',
    ];
    protected $casts = [
        'quantity' => 'decimal:2',
    ];
    public function order()
    {
        return $this->belongsTo(Order::class);  
    }
    
}
